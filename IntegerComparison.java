public class IntegerComparison {
    public static void main(String[] args) {
        Integer a = 100, b = 100;
        System.out.println(a == b);
        System.out.println(a.equals(b));

        Integer x = 200, y = 200;
        System.out.println(x == y);
        System.out.println(x.equals(y));
    }
}